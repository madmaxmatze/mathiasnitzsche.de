<h3>What I have learned</h3>
<p>
	But more interesting then showing what I have done, is to tell what I have learned:
</p>
	<blockquote><b>Teamsite brings out the best and worst of software engineering!</b></blockquote>
<p>
	Of course you can have a <i>scheduled job</i>, using <i>Perl</i> to generate <i>PHP</i>, which reads <i>JavaScript</i> from a <i>database</i> to create <i>HTML</i>. Maybe you even find a place for XSLT and stored procedures. Then embed this into a <i>3-stage-group-workflow</i> accross a <i>Dev-Test-Stag-Prod-environment</i> with aditional <i>search</i>, <i>email</i> and <i>authorisation servers</i> and you will have a well paid consulting job for the next years... Just make sure I don't find you!
</p>	
	
	
	
<h3>Different approach to Teamsite</h3>
<p>
	For every project (and especially if Teamsite is involved) just remember two easy principles:<br>
	<a style="font-weight: bold; margin-left: 200px; font-size: 120%" href="http://en.wikipedia.org/wiki/KISS_principle">Keep it simple stupid!</a><br>
	<a style="font-weight: bold; margin-left: 190px; font-size: 120%" href="http://en.wikipedia.org/wiki/You_ain't_gonna_need_it">You ain't gonna need it</a>
</p>
<p>
	So what I tend to do, is to radicaly get rid of as much teamsite specific stuff as possible and replace it with application logic. And whatever issue pops up, my answer will always be: <i>Let our web application or even better the underlying framework(!) handle this.</i> The result is usually a reusable, fail tolerant and extremly performant web application which works directly on xml/dcr files (no presentation templates!). Customers understand and love their web-environment again and I can be proud of my work - even if Teamsite is involved :)
</p>
	




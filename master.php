<h3>Masterthesis</h3>
<p>
	In 2011 I wrote my Master Thesis as a member of the <a href="http://www.stratosphere.eu" target="_blank">Stratosphere-Project</a> in the institute <a href="http://www.dbis.informatik.hu-berlin.de/" target="_blank">Databases and Information Systems</a>. Within the Stratosphere Project PACT-programs were used to process large amounts of data in parallel on a distributed cluster computer architecture. However creating a PACT-program was very time-consuming, complicated, and required the installation of a development environment. My master thesis describes the implementation of the <a href="http://code.google.com/webtoolkit/" target="_blank">Google Web Toolkit</a> based PACT-GUI web application. The application allows to graphically model and implement PACT-programs within a rich client browser application. In future, the ease with which users can now generate PACT-programs may promote the wide adoption of Stratosphere System.
</p>

